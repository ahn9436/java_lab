class Main {
    public static void main(String[] args) {

        // create an array
        int[] age = {12, 4, 5, 152, 515, 515, 8785};


        //loop through the array
        //using for loop
        System.out.println("Using for each Loop:");
        for(int i : age) {
            System.out.println(i);
        }
    }
}